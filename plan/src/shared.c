void braking_sequence(int cycles, int delay)
{
	int i = 0;

	for(i = 0; i < cycles; i++)
	{
		tx_cmd_ctrl_o_write(FLEXSEA_EXECUTE_1, -400);
		send_cmd_slave();
		usleep(delay);
		tx_cmd_ctrl_o_write(FLEXSEA_EXECUTE_1, 0);
		send_cmd_slave();
		usleep(delay);
	}
}
