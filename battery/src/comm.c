//Prepares the buffer
void init_ezI2Cbuf(void)
{
	int i = 0;
	
	//Master write: all 0
	for(i = 0; i < EZI2C_WBUF_SIZE; i++)
	{
		ezI2Cbuf[i] = 0;
	}
	//Master read: test data
	for(i = EZI2C_WBUF_SIZE; i < EZI2C_BUF_SIZE; i++)
	{
		//ezI2Cbuf[i] = i;	//Test only
		ezI2Cbuf[i] = 0;
	}
}
